$("#form-apriori :checkbox").change(function () {
    $(this).each(function () {
        if ($(this).is(":checked")) {
            if ($(this).length == 0) {
                sumbitApriori.prop("disabled", true);
            } else {
                sumbitApriori.prop("disabled", false);
            }
        } else {
            sumbitApriori.prop("disabled", true);
        }
    });
});
